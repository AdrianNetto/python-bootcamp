# Sum
print(3 + 4)

# Subtraction
print(4 - 3)

# Division
print(4 / 2)

# Multiplication
print(4 * 2)

# Modulo or "Mod" Operator
print(7 % 3) # returns 3

# - How to know if a number is even or odd
print(23 % 2) # make "% 2", and need to return 0 to be even.

# Powers
print(2 ** 3)

# Resolve first some part of the calc
print(2 + 10 * 10 + 3) # Follow the PEMDAS pattern, resolving first the multiplication, and it returns 105.

print((2 + 10) * (10 + 3)) # Also follow the PEMDAS pattern, but now it resolves first the parentheses, and  this retuns 156.

